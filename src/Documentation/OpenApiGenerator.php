<?php

namespace ApiForge\Documentation;

use ApiForge\Support\EndpointDescriptor;
use ApiForge\Support\RegistrySnapshot;
use ApiForge\Support\ValidationRuleParser;

class OpenApiGenerator
{
    /**
     * @return array<string, mixed>
     */
    public function generate(RegistrySnapshot $registry): array
    {
        $paths = [];

        foreach ($registry->endpoints as $endpoint) {
            $paths = $this->mergePath($paths, $endpoint);
        }

        return [
            'openapi' => '3.1.0',
            'info' => [
                'title' => (string) config('apiforge.documentation.title', 'API Documentation'),
                'version' => (string) config('apiforge.documentation.version', '1.0.0'),
                'description' => 'Generated by ApiForge',
            ],
            'paths' => $paths,
            'components' => [
                'securitySchemes' => [
                    'ApiKeyAuth' => [
                        'type' => 'apiKey',
                        'in' => 'header',
                        'name' => (string) config('apiforge.keys.header', 'X-Api-Key'),
                    ],
                ],
            ],
        ];
    }

    /**
     * @param  array<string, mixed>  $paths
     * @return array<string, mixed>
     */
    protected function mergePath(array $paths, EndpointDescriptor $endpoint): array
    {
        $uri = $endpoint->endpoint;
        $method = strtolower($endpoint->method);

        $operation = [
            'operationId' => $this->operationId($endpoint),
            'summary' => $endpoint->name,
            'description' => $endpoint->description,
            'tags' => [$endpoint->version],
            'responses' => [
                '200' => [
                    'description' => 'Successful response',
                ],
            ],
            'x-apiforge-roles' => $endpoint->roles,
            'x-apiforge-scopes' => $endpoint->scopes,
        ];

        if ($endpoint->auth) {
            $operation['security'] = [['ApiKeyAuth' => []]];
        }

        if ($endpoint->payload !== []) {
            $properties = [];
            $required = ValidationRuleParser::requiredFields($endpoint->payload);

            foreach ($endpoint->payload as $field => $rules) {
                $properties[$field] = ValidationRuleParser::toOpenApiProperty((string) $rules);
            }

            $operation['requestBody'] = [
                'required' => $required !== [],
                'content' => [
                    'application/json' => [
                        'schema' => [
                            'type' => 'object',
                            'properties' => $properties,
                            'required' => $required,
                        ],
                    ],
                ],
            ];
        }

        $paths[$uri] ??= [];
        $paths[$uri][$method] = array_filter($operation, fn ($value) => $value !== null && $value !== []);

        return $paths;
    }

    protected function operationId(EndpointDescriptor $endpoint): string
    {
        $slug = preg_replace('/[^a-zA-Z0-9_]+/', '_', strtolower($endpoint->name)) ?: 'endpoint';

        return trim($slug, '_').'_'.strtolower($endpoint->method);
    }
}
